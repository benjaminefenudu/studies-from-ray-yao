// FS Methods
// The following chart lists other frequently-used “fs” methods:

// fs.access(file, mode, callback)
// Check the permission of a specified file

// fs.appendFile(filename, data, callback)
// Append contents to a file

// fs.chmod(file, mode, callback)
// Change the permission of a file

// fs.exists(file, callback)
// Check whether the file exists

// fs.link(path1, path2, callback(err))
// Create a link between two paths

// fs.truncate(file, length, callback)
// Truncate the contents of a file

// fs.utimes(file, access_time, modify_time, callback)
// Modify the timestamp of a file

// fs.watchFile(file, listener)
// View the modification of a file